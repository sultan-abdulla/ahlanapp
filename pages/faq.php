<script>

  //display show more button 
  $(document).ready(function () {

    // hide all articles
    $(".card.accordion-card").hide();

    //  get the number of articles
     number_of_items = $(".card.accordion-card").length;

    //  display only 4 items
    items_to_display = 10;

    // check if number of articles is less than 4 then hide show more
    if (items_to_display >= number_of_items) {
      $(".view-more").hide();
    }

    //  display only 4 items
    $(".card.accordion-card:lt(" + items_to_display + ")").show();


    // when user click show more button
    $(".view-more").click(function () {
      // add 2 to number of articles
      if (items_to_display + 5 < number_of_items) {
        items_to_display = items_to_display +5;
        // if sum less than total number make the new display number + 2 
      } else {
        // if sum bigger than total number  then display all
        items_to_display = number_of_items;
        // hide show more button
        $(".view-more").hide();
      }

      $(".card.accordion-card:lt(" + items_to_display + ")").show();
    });

  });
</script>
